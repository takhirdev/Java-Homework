public class Right_triangle {
    // input value INT
    public int Perimetr(int a, int b, int c) {
        int perimetr = a + b + c;
        return perimetr;
    }
    // input value DOUBLE
    public double Perimetr(double a, double b, double c) {
        double perimetr = a + b + c;
        return perimetr;
    }
    // input value FLOAT
    public float Perimetr(float a, float b, float c) {
        float perimetr = a + b + c;
        return perimetr;
    }
    // input value LONG
    public long Perimetr(long a, long b, long c) {
        long perimetr = a + b + c;
        return perimetr;
    }
    // input value INT
    public int Yuzi(int a, int b){
        int yuzi = (a * b) / 2;
        return yuzi;
    }
    // input value DOUBLE
    public double Yuzi(double a, double b){
        double yuzi = (a * b) / 2;
        return yuzi;
    }
    // input value FLOAT
    public float Yuzi(float a, float b){
        float yuzi = (a * b) / 2;
        return yuzi;
    }
    // input value LONG
    public long Yuzi(long a, long b){
        long yuzi = (a * b) / 2;
        return yuzi;
    }
}
